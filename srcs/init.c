#include "fractol.h"

int		init_env(t_env *e)
{
	if (!(e->mlx = mlx_init()))
		return (0);
	if (!(e->win = mlx_new_window(e->mlx, WIN_X, WIN_Y, "Fract'ol")))
		return (0);
	return (1);
}
